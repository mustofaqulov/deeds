export function getDailyHifzQuote() {
  const dayKey = new Date().toISOString().slice(0, 10);
  let hash = 0;
  for (let i = 0; i < dayKey.length; i++) hash += dayKey.charCodeAt(i);
  return HIFZ_QUOTES[hash % HIFZ_QUOTES.length];
}
